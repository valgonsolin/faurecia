<h2>QRCI</h2>
    <p>Quick Response-Continous Improvement ou Réponse rapide amélioration continue</p>

    <h4>Objectif : </h4>

    <p>Le QRCI constitue une <b>activité quotidienne</b> pour améliorer <b>la performance</b>, résoudre <b>des problèmes</b> et en <b>tirer les leçons</b> pour l'avenir.</p>
    <div class="center_illustration"><img class="illustration center-block" src="ressources/Basic07.PNG"></div>

    <h4>Moyen:Le tableau QRCI</h4>
    <p>On retrouve un tableau QRCI ligne au niveau de chaque ligne de production. le QRCI ligne nécessite l'implication des opérateurs, du Gap Leader, du Superviseur et des fonctions supports de l'UAP, il permet aussi de communiquer tout problème rencontré aux autres équipes.</p>
    <div class="center_illustration"><img class="illustration center-block" src="ressources/QRCI_01.PNG"></div>

    <h4>QRCI: une méthode de résolution des problèmes</h4>
    <p>Le QRCI est une méthode simple pour résoudre des problèmes et revenir à une situation normale. cette méthode est constituée de 2 étapes :</p>
    <ul>
        <li>Une étape de <b>sécurisation</b> qui doit être validée par les 3 équipes dans les 24h.</li>
        <li>Une étape d'<b>amélioration</b></li>
    </ul>
    <p>Le QRCI est une méthode basée sur des faits et des données et non sur des hypothèses, appliquable à tous les domaines (sécutité, qualité, maintenance, rebuts). cette dernière est décrite, en détail, au niveau du tableau QRCI dans chaque GAP.</p>

